package com.kodilla.abstracts.basic_assertion;
import java.lang.Math;
public class Calculator {
    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
    public double power(double a, double b) {
        return Math.pow(a, b);
    }
    }


